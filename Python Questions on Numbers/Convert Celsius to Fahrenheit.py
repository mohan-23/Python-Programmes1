# F = (C * 1.8) + 32, Where F is the temperature in Fahrenheit, and C
# is the temperature in Celsius

# (Celsius To Fahrenheit)
celsius = float(30)
fahrenheit = (celsius * 1.8) + 32
print("Temperature in Fahrenheit: ", fahrenheit)
print('-----------')

# (Fahrenheit To Celsius)
# C = ((F - 32)*5)/9
fahrenheit = float(96.8)
celsius = ((fahrenheit-32)*5)/9
print("Celsius=", celsius)

# Output 1
# Fahrenheit Temperature = 96.8
# Celsius Temperature = ((96.8-32)*5)/9 = 36.0

# Output 2
# Celsius Temperature = 54
